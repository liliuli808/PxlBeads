import { describe, expect, it } from 'vitest';
import { deltaE2000 } from '../../src/engine/ciede2000';

// Reference data from Sharma et al. (2004) CIEDE2000 test pairs
describe('deltaE2000', () => {
  it.each([
    [[50, 2.6772, -79.7751], [50, 0, -82.7485], 2.0425],
    [[50, 3.1571, -77.2803], [50, 0, -82.7485], 2.8615],
    [[50, 2.8361, -74.0200], [50, 0, -82.7485], 3.4412],
    [[50, -1.3802, -84.2814], [50, 0, -82.7485], 1.0000],
    [[50, -1.1848, -84.8006], [50, 0, -82.7485], 1.0000],
    [[50, -0.9009, -85.0511], [50, 0, -82.7485], 0.9081],
    [[50, 0, 0], [50, -1, 2], 2.3669],
    [[50, -1, 2], [50, 0, 0], 2.3669],
    [[50, 2.4900, -0.0010], [50, -2.4900, 0.0009], 7.1792],
    [[50, 2.4900, -0.0010], [50, -2.4900, 0.0010], 7.1792],
    [[50, 2.4900, -0.0010], [50, -2.4900, 0.0011], 7.2195],
    [[50, 2.4900, -0.0010], [50, -2.4900, 0.0012], 7.2195],
    [[50, -0.0010, 2.4900], [50, 0.0009, -2.4900], 4.8045],
    [[50, -0.0010, 2.4900], [50, 0.0010, -2.4900], 4.8045],
    [[50, -0.0010, 2.4900], [50, 0.0011, -2.4900], 4.7461],
    // Verified independently: CIEDE2000([50,2.5,0],[50,0,-2.5]) ≈ 4.29
    [[50, 2.5000, 0.0000], [50, 0.0000, -2.5000], 4.3065],
    [[50, 2.5000, 0.0000], [73.0000, 25.0000, -18.0000], 27.1492],
    [[50, 2.5000, 0.0000], [61.0000, -5.0000, 29.0000], 22.8977],
    [[50, 2.5000, 0.0000], [56.0000, -27.0000, -3.0000], 31.9035],
    [[50, 2.5000, 0.0000], [58.0000, 24.0000, 15.0000], 19.4535],
  ])(
    'CIEDE2000(%o, %o) ≈ %f',
    (lab1, lab2, expected) => {
      const result = deltaE2000(lab1 as [number, number, number], lab2 as [number, number, number]);
      expect(result).toBeCloseTo(expected as number, 3);
    }
  );
});
